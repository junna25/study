public class Array5 {
    public static void main(String[] args) {
        int [][] miyu = new int[2][2];
        miyu[0][0] = 1;
        miyu[0][1] = 2;
        miyu[1][0] = 10;
        miyu[1][1] = 11;

        System.out.println("miyu[0][0] = " + miyu[0][0]);
        System.out.println("miyu[0][0] = " + miyu[0][1]);
        System.out.println("miyu[0][0] = " + miyu[1][0]);
        System.out.println("miyu[0][0] = " + miyu[1][1]);

    }
}

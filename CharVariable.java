public class CharVariable {
    public static void main(String[] args) {
        char ch1 = '\u7d50';
        char ch2 = '\u57ce';
        System.out.println("ch1 = " + ch1);
        System.out.println("ch2 = " + ch2);

        char ch3 = '\u7f8e';
        char ch4 = '\u512a';
        System.out.println("ch3 = " + ch3);
        System.out.println("ch4 = " + ch4);
    }
}

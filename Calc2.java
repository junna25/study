public class Calc2 {
    static int add (int a, int b) {
        int answer = a + b;
        return answer;
    }

    static double add(double a, double b) {
        double answer = a + b;
        return answer;
    }

    static int add(int a, int b, int c) {
        int answer = a + b + c;
        return answer;
    }
}
